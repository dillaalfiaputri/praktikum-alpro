print('OPERATOR OR' )
print('Semua statement salah :',
(5<3) or (7<5))
print('Salah satu statement benar :',
(5<3) or (7>5))
print('--------------------')

print('OPERATOR NOT' )
print( 'Statement benar :',not(5>3))
print( 'Statement salah :',not(5<3))
print('--------------------')