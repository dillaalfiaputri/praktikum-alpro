a = 4
b = 6

#penjumlahan(+)
hasil = a + b
print('hasil penjumlahan:', hasil )

#pengurangan(-)
hasil = a - b
print('hasil pengurangan:', hasil)

#perkalian(*)
hasil = a * b
print('hasil perkalian:', hasil)

#pangkat(**)
hasil = a ** b
print('hasil perpangkat', hasil)

#pembagian(/)
hasil = a / b
print('hasil pembagian:', hasil)

#pembagian ke bawah(//)
hasil = a // b
print('hasil pembagian kebawah:', hasil)

#sisa bagi(%)
hasil = a % b
print('hasil sisa bagi:', hasil)

#precedence
hasil = 1 + 2 * 4 - 7
print('hasil precedence:', hasil)

#precedence
hasil = (1 + 2) * 4 - 7
print('hasil precedence:', hasil)